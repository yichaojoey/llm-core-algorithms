def one_f_one_b_scheduling():
    print("\n=" * 50)
    print("当代工业界极限挤牙膏之神流派 (1F1B 交错调度大阵)：")
    
    # 它极其违背常理地不一口气排队做完大锅饭前向，只要刚做完一片的 Forward 到最底层，底层毫不理会还没完成的其它前向，直接连着把刚才的那个 Backward 回滚踢向前方！
    print("卡 0: [F_1] [F_2] [F_3] [F_4] [B_1] [B_2] [B_3] [B_4] ")
    print("卡 1: (呆留) [F_1] [F_2] [B_1] [F_3] [B_2] [F_4] [B_3] [B_4]")
    print("-> 这里你能看极其神工鬼斧的一种错位填充！")
    print("因为卡 1 在做完最左侧底边的 1 号碎块 F_1 之后，直接立马回手掏进入 B_1 进行回传交公还给左侧！从而让第一张卡的 B_1 在这个时候完美丝滑顺流接棒插入！填在了前面一堆 F 的缝隙中！")
    print("这就逼迫：极其完美错峰交差。大家就像极其致密的咬合齿轮，没有任何一个人需要发呆留白一整片。虽然头尾没法避免起步阶段发呆，但是庞大的身体腹部交并极其厚实坚挺！")
    

