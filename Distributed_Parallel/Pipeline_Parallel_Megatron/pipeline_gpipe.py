"""
Pipeline Parallel (流水线兵法：GPipe 与 1F1B 交错调度)
===================================================
【理论揭秘】：
除了切数据 (DP) 和 竖着矩阵切分 (TP)。当你的层数实在太厚了咋办（比如高达 120 层的极深极高大厦）？
当然是一点一滴把它横向折成肉夹馍并拆开来放置入无数的服务器：
- GPU 0：包揽第 1-30 层
- GPU 1：拿走第 31-60 层 ... (俗称 Pipeline Parallelism，PP 流水线并行)
但极其恐怖恶心的是：GPU 1 必须像个煞笔一样死死坐在那里等极其久远的时间，等到最上面的 GPU 0 把结果一层一层算完慢悠悠从网线滴答传输下来才能开始干活！（这叫【气泡气柱 Bubble】。这段时间 GPU 1 的算力和钞票燃烧全被白白空转浪费！）

所以神操作登场：**如何巧妙地往这个流水线极其紧凑严丝合缝地在所有角落极高压地塞进各种细碎碎肉任务小批次（Micro-Batch）让所有卡不能有一丁点歇息呢？**
这就不得不祭出臭名昭著让无数学者秃头心酸的神级图表算法了：【1F1B 调度】 (One Forward + One Backward 直接连贯)！
"""

